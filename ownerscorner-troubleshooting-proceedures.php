			<p  class="title">Pump Priming Instructions</p>
			<p><strong>This method must be used for the jets pumps because these pumps are not connected to the main filter system in any way.</strong></p>
			<p>
				<ol>
					<li>Turn off the power to the spa.</li>
					<li>Remove the handle from the massage selector supplied by the pump you are priming.</li>
					<li>Loosen the massage selectorÕs cap slightly (counterclockwise), listening for the air to seep out.</li>
					<li>Tighten the cap finger tight, replace the handle, and turn the spaÕs power back on.</li>
				</ol>
			</p>
			<div class="caution"><p>If freezing conditions exist and pump is not operational, take measures to protect the system from freeze damage. See Section title 'Winterzing', page 66.</p></div>
		</div>

		<div class="row">
			<p  class="title">Poor jet action (Sections 9.4-9, 25, pages 24-45)</p>
			<p><strong>Procedure:</strong></p>
			<p>
				<ol>
					<li>Make certain the jets pump is turned on.</li>
					<li>Check position of massage selectors</li>
					<li>Open air control for selected jet system.</li>
					<li>Check for adequate water level.</li>
					<li>Check for dirty filter (pages 60-62)</li>
				</ol>
			</p>
		</div>

		<div class="row">
			<p  class="title">Water is too hot.</p>
			<p><strong>Procedure:</strong></p>
			<p>
				<ol>
					<li>Reduce thermostat setting.</li>
					<li>Switch hot tub to 'Economy' mode to turn heater on only during programmed filter cycles (page 54).</li>
				</ol>
			</p>
		</div>

		<div class="row">
			<p  class="title">No heat</p>
			<p><strong>Procedure:</strong></p>
			<p>
				<ol>
					<li>Check thermostat setting.</li>
					<li>Keep the cover in place while heating.</li>
					<li>If 'heater on' indictor is lit but no temperature rise is experienced after a reasonable period of time, contact your Sundance Spas dealer or qualified service technician</li>
				</ol>
			</p>
		</div>

		<div class="row">
			<p  class="title">Filter/circulation pump off</p>
			<p><strong>Procedure:</strong></p>
			<p>
				<ol>
					<li>Verify filter/circulation pump is programmed to run 8 hours a day (page 54).</li>
					<li>Filter/circulation pump cycle canceled by power interruption. Wait 24 hours for hot tub to reset or press select button for manual activation.</li>
					<li>Hot tub water is warmer than 95&deg;F (35&deg;C) and two degrees warmer than the set temperature. The 'Summer Logic' safety feature has activated. See not on page 55 for details.</li>
				</ol>
			</p>
		</div>
